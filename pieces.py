def piece_unicode(piece: chess.Piece | None) -> str:
    if piece is None:
        return ""
    return UNICODE_PIECES[piece.symbol()]


def symbol_unicode(symbol: str) -> str:
    return UNICODE_PIECES.get(symbol, "")


def positional_bonus(piece: chess.Piece, square: chess.Square) -> int:
    table = PIECE_SQUARE_TABLES.get(piece.piece_type)
    if table is None:
        return 0
    index = square if piece.color == chess.WHITE else chess.square_mirror(square)
    return table[index]
